    """
    Look up the CBSA code and MSA name for a county.

    Args:
        state_fips: 2-digit state FIPS code (e.g., "04" for Arizona)
        county_fips: 3-digit county FIPS code (e.g., "013" for Maricopa)

    Returns:
        Tuple of (cbsa_code, msa_name) or None if county is not in a metro area.
    """
